	org $0000
	CLR 0030H
	CLR C
	MOV A,#2AH
	MOV B,#02H 
	MOV R1,B
	CJNE R1,#00H,OK
	MOV 0030H,#01H
SJMP ENDLOOP
OK:CLR C
	MOV R0,A
	MOV A,B
	RLC A
	RLC A
	JC ERR
	MOV 0020H,A
	MOV B,A
	MOV A,R0
	MOV R2,#00H 
BDIV:SUBB A,B
        JC OVER
	INC R2
        SJMP BDIV
OVER: ADD A,B
	MOV 0022H,A
	MOV 0021H,R2
	MOV 0023H,R2
	MOV 0030H,#00H
ENDLOOP: SJMP ENDLOOP
ERR:MOV 0023H,A
	MOV 0030H,#02H
SJMP ENDLOOP 
	